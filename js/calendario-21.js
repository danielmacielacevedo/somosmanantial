const maestro1 = document.querySelector('.maestro1');
const dataMaestro1 = document.querySelector('.table-data1');
let verifyMaestro1 = false;


maestro1.addEventListener('click', () => {
    if(!verifyMaestro1) {
        dataMaestro1.classList.add('on');
        verifyMaestro1 = true;
    } else {
        dataMaestro1.classList.remove('on')
        verifyMaestro1 = false;
    }
});

